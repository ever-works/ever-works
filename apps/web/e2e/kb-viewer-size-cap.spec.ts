/**
 * EW-641 Phase 1B/d row 21c â€” A14 acceptance e2e (viewer size caps).
 *
 * Drives the per-MIME viewer dispatcher end-to-end through the new
 * KB "workbench" UI (`KbDocumentViewerSwitch`), which REUSES the same
 * `Kb{Pdf,Xlsx,â€¦}Viewer` components (and their `kb-pdf-viewer` /
 * `kb-xlsx-viewer` testids) as the old detail page, but now wraps each
 * one in an OUTER `SizeThresholdGate` (`kb-workbench-size-gate` /
 * `kb-workbench-size-blocked`). That gives TWO tiers of size caps:
 *
 *   - the workbench gate (`SizeThresholdGate`): PDF 50 MiB, XLSX 15 MiB
 *     â€” short-circuits to a download banner BEFORE the viewer mounts.
 *   - the viewer's own inline cap: PDF 30 MiB, XLSX 5 MiB â€” decides
 *     `data-mode="inline"` vs `data-mode="download"` once mounted.
 *
 *  A14.1 â€” PDF under BOTH caps (a few-KB valid PDF < 30 MiB < 50 MiB):
 *    seed via the public upload endpoint, navigate to the per-doc
 *    workbench route, assert the workbench gate passed through and that
 *    `KbPdfViewer` mounts in `data-mode="inline"` (we only check the
 *    dispatcher's outer-container state, not the PDF.js render, so this
 *    stays fast and stable on CI).
 *
 *  A14.2 â€” XLSX in the band between the two caps (~6 MiB > 5 MiB viewer
 *    cap, but < 15 MiB workbench-gate cap): the workbench gate PASSES
 *    THROUGH (6 < 15) so the viewer mounts, then the viewer's own 5 MiB
 *    threshold flips it to `data-mode="download"` and renders the
 *    `kb-xlsx-download-fallback` card. We assert the gate did NOT block
 *    AND the viewer-side download fallback is visible.
 *
 * The viewer dispatcher is mounted by the workbench route
 * `apps/web/src/app/[locale]/(dashboard)/works/[id]/kb/[...path]/page.tsx`
 * â€” it fetches the source upload row when `doc.sourceUploadId` is set
 * and forwards `mimeType` / `fileSize` / `downloadUrl` to
 * `KbDocumentViewerSwitch`, which branches on the bare MIME to the
 * matching `Kb{Pdf,Xlsx,Docx,Image,Video,Audio}Viewer`. The download
 * URL points at the row-21a proxy
 * (`/api/works/:id/kb/uploads/:uploadId/download`).
 *
 * Lives in `apps/web/e2e/` so the existing `e2e.yml` workflow picks it
 * up on push to develop / stage / main. Authenticated by the shared
 * `storageState` from `global-setup.ts` (the chromium project's
 * `testIgnore` regex does not enumerate `kb-*`).
 */

const PDF_MIME = 'application/pdf';
const XLSX_MIME = 'application/vnd.openxmlformats-officedocument.spreadsheetml.sheet';

/**
 * Build a minimal-but-valid PDF with one page that draws a few lines of
 * text via the standard Helvetica font, using `pdf-lib`. The agent's
 * pdf-parse extractor (an old pdfjs fork) needs BOTH (a) a parseable
 * `/Catalog` + `/Pages` tree AND (b) at least one page that yields > 0
 * chars of text â€” otherwise the extractor reports `"KB PDF extraction
 * produced no text â€” likely an image-only PDF; OCR is Phase 3"` and
 * marks the upload row failed.
 *
 * Earlier hand-rolled attempts (commits 32df2af6, 21680fa5, 5f555b85)
 * failed inside pdf-parse with various permutations of "Command token
 * too long: 128" and "bad XRef entry" even with the binary marker and
 * byte-perfect xref offsets. pdfjs's tolerance for terse-but-valid
 * PDFs is essentially zero. Using `pdf-lib` shifts the byte-layout
 * correctness burden to a battle-tested PDF library; it's already a
 * tiny pure-JS dependency with no native bindings.
 *
 * We're only asserting on the viewer dispatcher's `data-mode`
 * attribute, not on the actual rendered content, so a one-page PDF
 * with three Helvetica text lines is plenty.
 */
async function makeMinimalValidPdf(): Promise<Buffer> {
    const { PDFDocument, StandardFonts } = await import('pdf-lib');
    const pdfDoc = await PDFDocument.create();
    const font = await pdfDoc.embedFont(StandardFonts.Helvetica);
    const page = pdfDoc.addPage([612, 792]);
    page.drawText('Hello, World!', { x: 50, y: 720, size: 24, font });
    page.drawText('KB viewer dispatcher test fixture.', {
        x: 50,
        y: 690,
        size: 16,
        font,
    });
    page.drawText('Generated by kb-viewer-size-cap.spec.ts', {
        x: 50,
        y: 660,
        size: 16,
        font,
    });
    return Buffer.from(await pdfDoc.save());
}

/**
 * Build a real `.xlsx` byte buffer (ZIP-of-XML, exceljs-emitted) padded
 * with incompressible random base64 cells until the file is at least
 * `targetBytes`. The agent's buffer extractor parses every uploaded
 * `application/vnd...sheet` body via `exceljs.xlsx.load()`, so handing
 * it `Buffer.alloc(6 * 1024 * 1024, 0)` produced a "not a zip file"
 * extraction failure that left the upload row with `document: null`.
 *
 * For the A14.2 dispatcher test we just need a workbook that:
 *  - extracts cleanly (so the API returns `{upload, document}`),
 *  - is > KbXlsxViewer's 5 MiB inline cap (so the viewer mounts in
 *    `data-mode="download"`).
 */
async function makeOversizeXlsx(targetBytes: number): Promise<Buffer> {
    // exceljs ships as CJS; under Node ESM `await import('exceljs')`
    // returns `{ default: ExcelJSObject }` and the `.Workbook` constructor
    // lives on the default export â€” `new ExcelJS.Workbook()` directly on
    // the namespace throws "is not a constructor". The production
    // `load-exceljs-workbook.ts` only works because webpack flattens the
    // interop in the browser bundle; in Playwright (raw Node) we have to
    // unwrap the default ourselves. Fall through to the namespace for
    // ESM-native builds that do not wrap.
    interface ExcelJsCtorHost {
        Workbook: new () => {
            addWorksheet(name: string): {
                getCell(addr: string): { value: string };
            };
            xlsx: { writeBuffer(): Promise<ArrayBuffer> };
        };
    }
    const mod = (await import('exceljs')) as unknown as {
        default?: ExcelJsCtorHost;
    } & ExcelJsCtorHost;
    const ExcelJS: ExcelJsCtorHost = mod.default ?? mod;
    const { randomBytes } = await import('node:crypto');
    const wb = new ExcelJS.Workbook();
    const sheet = wb.addWorksheet('Sheet1');
    // 64 KiB random â†’ ~88 KiB base64 per cell, ZIP-incompressible.
    const CHUNK = 64 * 1024;
    const rowsNeeded = Math.ceil(targetBytes / CHUNK) + 8;
    for (let i = 0; i < rowsNeeded; i++) {
        sheet.getCell(`A${i + 1}`).value = randomBytes(CHUNK).toString('base64');
    }
    return Buffer.from(await wb.xlsx.writeBuffer());
}

test.describe('Knowledge Base â€” A14 viewer size caps', () => {
    test('PDF under 30 MiB renders inline via KbPdfViewer', async ({ page, request }) => {
        test.setTimeout(180_000);

        const testUser = loadSeededTestUser();
        const { access_token } = await loginViaAPI(request, {
            email: testUser.email,
            password: testUser.password,
        });

        const runId = `${Date.now().toString(36)}${Math.random().toString(36).slice(2, 6)}`;
        const { id: workId } = await createWorkViaAPI(request, access_token, {
            name: `KB PDF inline ${runId}`,
        });
        expect(workId, 'createWorkViaAPI must return a non-empty work id').toBeTruthy();

        const { path: docPath } = await seedKbBinaryDoc(request, access_token, workId, {
            filename: `kb-a14-pdf-${runId}.pdf`,
            mimeType: PDF_MIME,
            body: await makeMinimalValidPdf(),
        });
        // The server stores every extracted upload at `${class}/${slug}.md`
        // (knowledge-base.service.ts â‰ˆ line 1496); the original `.pdf`
        // extension is dropped during extraction. `KbDocumentViewerSwitch`
        // then picks `KbPdfViewer` from `upload.mimeType`, NOT from the
        // document path, so any matching slug+class is fine here.
        expect(docPath).toMatch(/^freeform\/kb-a14-pdf-.*\.md$/);

        await page.goto(`/en/works/${workId}/kb/${docPath}`, { waitUntil: 'domcontentloaded' });

        // The workbench wraps the viewer in `SizeThresholdGate`. A few-KB
        // PDF is well under the gate's 50 MiB PDF cap, so the gate renders
        // in pass-through mode and never shows its own size-blocked banner.
        await expect(page.locator('[data-testid="kb-workbench-size-blocked"]')).toHaveCount(0);

        const pdfViewer = page.locator('[data-testid="kb-pdf-viewer"]');
        await expect(pdfViewer).toBeVisible({ timeout: 60_000 });
        await expect(pdfViewer).toHaveAttribute('data-mode', 'inline');
        // Confirm the viewer's own download fallback did NOT render â€” the
        // inline branch chose KbPdfViewerCanvas (next/dynamic), not the
        // download-fallback card.
        await expect(page.locator('[data-testid="kb-pdf-download-fallback"]')).toHaveCount(0);
    });

    test('XLSX over 5 MiB shows download fallback via KbXlsxViewer', async ({ page, request }) => {
        test.setTimeout(180_000);

        const testUser = loadSeededTestUser();
        const { access_token } = await loginViaAPI(request, {
            email: testUser.email,
            password: testUser.password,
        });

        const runId = `${Date.now().toString(36)}${Math.random().toString(36).slice(2, 6)}`;
        const { id: workId } = await createWorkViaAPI(request, access_token, {
            name: `KB XLSX over-cap ${runId}`,
        });
        expect(workId).toBeTruthy();

        // Real exceljs-emitted workbook padded with random base64 cells
        // to ~6 MiB. Has to be a valid XLSX so the agent's buffer
        // extractor (`exceljs.xlsx.load()`) doesn't reject the upload â€”
        // a zero-filled buffer used to fail extraction with "not a zip
        // file" and leave the upload row with `document: null`. The
        // viewer dispatcher then decides on `fileSize` AFTER the row
        // exists, so a slightly oversized real workbook is the cleanest
        // way to drive the > 5 MiB download-fallback branch.
        //
        // 6 MiB is deliberately chosen to sit in the band BETWEEN the two
        // workbench caps: above the viewer's 5 MiB inline cap (so the
        // viewer flips to download mode) but below the `SizeThresholdGate`
        // 15 MiB XLSX cap (so the outer gate passes through and the viewer
        // actually mounts rather than the gate's own size-blocked banner).
        const xlsxBuffer = await makeOversizeXlsx(6 * 1024 * 1024);
        const { path: docPath } = await seedKbBinaryDoc(request, access_token, workId, {
            filename: `kb-a14-xlsx-${runId}.xlsx`,
            mimeType: XLSX_MIME,
            body: xlsxBuffer,
        });
        // Same `.md` rewrite as the PDF case â€” viewer dispatch reads
        // `upload.mimeType`, not the doc path extension.
        expect(docPath).toMatch(/^freeform\/kb-a14-xlsx-.*\.md$/);

        await page.goto(`/en/works/${workId}/kb/${docPath}`, { waitUntil: 'domcontentloaded' });

        // The outer workbench `SizeThresholdGate` must PASS THROUGH here
        // (6 MiB < its 15 MiB XLSX cap), so it renders the viewer rather
        // than its own `kb-workbench-size-blocked` banner. If this count
        // were non-zero the workbench gate would have pre-empted the
        // viewer-side fallback we actually want to exercise.
        await expect(page.locator('[data-testid="kb-workbench-size-blocked"]')).toHaveCount(0);

        const xlsxViewer = page.locator('[data-testid="kb-xlsx-viewer"]');
        await expect(xlsxViewer).toBeVisible({ timeout: 60_000 });
        await expect(xlsxViewer).toHaveAttribute('data-mode', 'download');
        await expect(page.locator('[data-testid="kb-xlsx-download-fallback"]')).toBeVisible({
            timeout: 10_000,
        });
        // The download anchor's href must point at the row-21a download
        // proxy so the operator's click streams the bytes back through
        // the same CSP+nosniff-pinned route.
        const downloadLink = page.locator('[data-testid="kb-xlsx-download-link"]');
        await expect(downloadLink).toBeVisible();
        await expect(downloadLink).toHaveAttribute(
            'href',
            new RegExp(`/api/works/${workId}/kb/uploads/[^/]+/download$`),
        );
    });
});
